《网》结尾的时候，故迟把那根绳索收进了木箱，放在解码器碎片的旁边。通讯器他修好了。他想，如果希米今天来就把绳子还给她；如果三天没来——他没想好。

他知道自己十六年前在工坊碰过的那个频率区间，出现在了一个十六岁姑娘的手指间。他还不知道怎么面对这件事。

希米照常生活。她的网修好了，那根绳子她没再问过。
